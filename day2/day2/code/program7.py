# arithmetic operations
# - performed on the numeric (int, float, complex) operands 

n1 = 20
n2 = 30

# addition
print(f"{n1} + {n2}  = {n1 + n2}")

# subtraction
print(f"{n1} - {n2}  = {n1 - n2}")

# multiplication
print(f"{n1} * {n2}  = {n1 * n2}")

# true division
# - always returns float value
print(f"{n1} / {n2}  = {n1 / n2}")

# floor division
# - always returns int value (by discarding the decimal value)
print(f"{n1} // {n2} = {n1 // n2}")

# modulo operation
# - returns remainder after the division operation
print(f"{n1} % {n2}  = {n1 % n2}")

# power of (exponent operation)
# - return power of a number
print(f"{n1} ** 2  = {n1 ** 2}")

print('-' * 80)

# repeatition operation
# - performing an operation on a string with an integer operand

print('*' * 10)
print('*-*' * 10)

print('-' * 80)

# string concatinition
# - performing an concatinition operation on two string operands
str1 = 'sunbeam'
str2 = 'institue'
print(f"{str1} + ' ' + {str2} = {str1 + ' ' + str2}")
